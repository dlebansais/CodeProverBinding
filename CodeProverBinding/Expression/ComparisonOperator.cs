namespace CodeProverBinding;

/// <summary>
/// Comparison operators.
/// </summary>
public enum ComparisonOperator
{
    /// <summary>
    /// The &lt; operator.
    /// </summary>
    LessThan,

    /// <summary>
    /// The ≤ operator.
    /// </summary>
    LessThanOrEqual,

    /// <summary>
    /// The &gt; operator.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// The ≥ operator.
    /// </summary>
    GreaterThanOrEqual,
}
