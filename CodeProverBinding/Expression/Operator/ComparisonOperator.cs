namespace CodeProverBinding;

/// <summary>
/// Comparison operators.
/// </summary>
public enum ComparisonOperator
{
    /// <summary>
    /// The &lt; operator.
    /// </summary>
    LesserThan,

    /// <summary>
    /// The ≤ operator.
    /// </summary>
    LesserThanEqualTo,

    /// <summary>
    /// The &gt; operator.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// The ≥ operator.
    /// </summary>
    GreaterThanEqualTo,
}
